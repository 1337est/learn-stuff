# order of operations
number = 1 + 2 * 3 / 4.0
print(number)

# modulo
remainder = 11 % 3
print(remainder)

# exponents: number ** power_of
squared = 7 ** 2
cubed = 2 ** 3
print(squared)
print(cubed)

helloworld = "Hello" + " " + "world!"
print(helloworld)

lotsofhellos = "hello there | " * 10
print(lotsofhellos)

even_numbers = [2,4,6,8]
odd_numbers = [1,3,5,7,9]
all_numbers = odd_numbers + even_numbers
print(all_numbers)
